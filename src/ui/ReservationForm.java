    private void bnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnAjouterActionPerformed
        // TODO add your handling code here:
        try {
        int numChambre = Integer.parseInt(comboNumero.getSelectedItem().toString());
        int idClient = Integer.parseInt(comboClient.getSelectedItem().toString().split(" - ")[0]);

        Chambre ch = cs.findById(numChambre);
        Client cl = cls.findById(idClient);

        if (ch == null || cl == null) {
            JOptionPane.showMessageDialog(this, "Chambre ou Client introuvable !");
            return;
        }

        LocalDate d1 = txtDebut.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        LocalDate d2 = txtFin.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();

        // Confirmation popup before adding
        int confirmation = JOptionPane.showConfirmDialog(
            this, 
            "Voulez-vous vraiment ajouter la réservation?\n" +
            "Chambre: " + numChambre + "\n" +
            "Client: " + cl.getNom() + "\n" +
            "Du: " + d1 + " au: " + d2, 
            "Confirmation d'ajout de réservation", 
            JOptionPane.YES_NO_OPTION
        );
        
        if (confirmation == JOptionPane.YES_OPTION) {
            Reservation r = new Reservation(0, ch, cl, d1, d2);

            if (rs.create(r)) {
                JOptionPane.showMessageDialog(this, "Réservation ajoutée avec succès!");
                clearFields();
                loadData();
            } else {
                JOptionPane.showMessageDialog(this, "Erreur : chambre non disponible pour ces dates!");
            }
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur : " + e.getMessage());
        }
    }//GEN-LAST:event_bnAjouterActionPerformed

    private void bnSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnSupprimerActionPerformed
        // TODO add your handling code here:
        try {
        if (selectedId == -1) {
            JOptionPane.showMessageDialog(this, "Aucune réservation sélectionnée !");
            return;
        }

        Reservation r = rs.findById(selectedId);

        if (r == null) {
            JOptionPane.showMessageDialog(this, "Réservation introuvable !");
            return;
        }

        // Confirmation popup before deletion
        int confirmation = JOptionPane.showConfirmDialog(
            this, 
            "Voulez-vous vraiment supprimer la réservation #" + selectedId + "?\n" +
            "Chambre: " + r.getChambre().getNumero() + "\n" +
            "Client: " + r.getClient().getNom(), 
            "Confirmation de suppression", 
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE
        );
        
        if (confirmation == JOptionPane.YES_OPTION) {
            if (rs.delete(r)) {
                JOptionPane.showMessageDialog(this, "Réservation supprimée avec succès!");
                clearFields();
                loadData();
                selectedId = -1; // Reset selected ID
            } else {
                JOptionPane.showMessageDialog(this, "Erreur lors de la suppression!");
            }
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur : " + e.getMessage());
        }
    }//GEN-LAST:event_bnSupprimerActionPerformed

