    private void TableReservationMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableReservationMouseClicked
        // TODO add your handling code here:
        int row = TableReservation.getSelectedRow();

        selectedId = Integer.parseInt(TableReservation.getValueAt(row, 0).toString());

        // Chambre number from column 1
        String chambreNum = TableReservation.getValueAt(row, 1).toString();
        comboNumero.setSelectedItem(chambreNum);

        // Client NOM from column 2 → find matching "id - nom"
        String clientName = TableReservation.getValueAt(row, 2).toString();

        for (int i = 0; i < comboClient.getItemCount(); i++) {
            String item = comboClient.getItemAt(i).toString();
            if (item.endsWith(" - " + clientName) || item.contains(" " + clientName)) {
                comboClient.setSelectedIndex(i);
                break;
            }
        }

        // Dates
        txtDebut.setDate(java.sql.Date.valueOf(TableReservation.getValueAt(row, 3).toString()));
        txtFin.setDate(java.sql.Date.valueOf(TableReservation.getValueAt(row, 4).toString()));
    }//GEN-LAST:event_TableReservationMouseClicked

