    private void clearFields() {
        comboNumero.setSelectedIndex(-1);   // deselect chambre
        comboClient.setSelectedIndex(-1);   // deselect client
        txtDebut.setDate(null);
        txtFin.setDate(null);
    }


    private void loadData() {
        DefaultTableModel model = (DefaultTableModel) TableReservation.getModel();
        model.setRowCount(0);

        for (Reservation r : rs.findAll()) {
            model.addRow(new Object[]{
                r.getId(),
                r.getChambre().getNumero(),
                r.getClient().getNom(),
                r.getDateDebut(),
                r.getDateFin()
            });
        }
    }

    private void loadChambres() {
        comboNumero.removeAllItems();
        for (Chambre c : cs.findAll()) {
            comboNumero.addItem(String.valueOf(c.getNumero()));
        }
    }

    private void loadClients() {
        comboClient.removeAllItems();
        for (Client c : cls.findAll()) {
            comboClient.addItem(c.getId() + " - " + c.getNom());
        }
    }

